    @Test
    @Transactional
    void createClient() throws Exception {
        int databaseSizeBeforeCreate = clientRepository.findAll().size();
        // Create the Client
        restClientMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(client)))
            .andExpect(status().isCreated());

        // Validate the Client in the database
        List<Client> clientList = clientRepository.findAll();
        assertThat(clientList).hasSize(databaseSizeBeforeCreate + 1);
        Client testClient = clientList.get(clientList.size() - 1);
        assertThat(testClient.getAdvocate()).isEqualTo(DEFAULT_ADVOCATE);
        assertThat(testClient.getBiography()).isEqualTo(DEFAULT_BIOGRAPHY);
        assertThat(testClient.getBirthdate()).isEqualTo(DEFAULT_BIRTHDATE);
        assertThat(testClient.getFirstname()).isEqualTo(DEFAULT_FIRSTNAME);
        assertThat(testClient.getLastname()).isEqualTo(DEFAULT_LASTNAME);
        assertThat(testClient.getOabnumber()).isEqualTo(DEFAULT_OABNUMBER);
        assertThat(testClient.getPhone()).isEqualTo(DEFAULT_PHONE);
        assertThat(testClient.getCreatedAt()).isEqualTo(DEFAULT_CREATED_AT);
        assertThat(testClient.getPicture()).isEqualTo(DEFAULT_PICTURE);
        assertThat(testClient.getPictureContentType()).isEqualTo(DEFAULT_PICTURE_CONTENT_TYPE);
    }

