    @Test
    @Transactional
    void putNewClient() throws Exception {
        // Initialize the database
        clientRepository.saveAndFlush(client);

        int databaseSizeBeforeUpdate = clientRepository.findAll().size();

        // Update the client
        Client updatedClient = clientRepository.findById(client.getId()).get();
        // Disconnect from session so that the updates on updatedClient are not directly saved in db
        em.detach(updatedClient);
        updatedClient
            .advocate(UPDATED_ADVOCATE)
            .biography(UPDATED_BIOGRAPHY)
            .birthdate(UPDATED_BIRTHDATE)
            .firstname(UPDATED_FIRSTNAME)
            .lastname(UPDATED_LASTNAME)
            .oabnumber(UPDATED_OABNUMBER)
            .phone(UPDATED_PHONE)
            .createdAt(UPDATED_CREATED_AT)
            .picture(UPDATED_PICTURE)
            .pictureContentType(UPDATED_PICTURE_CONTENT_TYPE);

        restClientMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedClient.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedClient))
            )
            .andExpect(status().isOk());

        // Validate the Client in the database
        List<Client> clientList = clientRepository.findAll();
        assertThat(clientList).hasSize(databaseSizeBeforeUpdate);
        Client testClient = clientList.get(clientList.size() - 1);
        assertThat(testClient.getAdvocate()).isEqualTo(UPDATED_ADVOCATE);
        assertThat(testClient.getBiography()).isEqualTo(UPDATED_BIOGRAPHY);
        assertThat(testClient.getBirthdate()).isEqualTo(UPDATED_BIRTHDATE);
        assertThat(testClient.getFirstname()).isEqualTo(UPDATED_FIRSTNAME);
        assertThat(testClient.getLastname()).isEqualTo(UPDATED_LASTNAME);
        assertThat(testClient.getOabnumber()).isEqualTo(UPDATED_OABNUMBER);
        assertThat(testClient.getPhone()).isEqualTo(UPDATED_PHONE);
        assertThat(testClient.getCreatedAt()).isEqualTo(UPDATED_CREATED_AT);
        assertThat(testClient.getPicture()).isEqualTo(UPDATED_PICTURE);
        assertThat(testClient.getPictureContentType()).isEqualTo(UPDATED_PICTURE_CONTENT_TYPE);
    }

